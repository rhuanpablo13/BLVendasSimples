    @Override
    protected final void initMasks() {
        validator = new ViewValidator();
        validator.required(fCodigo);
        validator.required(fNome);
        validator.fieldCpf(fCpf, "", true, "isCpfValid", new Validador(), String.class);
    }
    
    
    @Override
    protected void cadastrar() {
        
        NCliente cliente = new NCliente();
        CCliente controller = new CCliente();
        
        cliente.setCadastroRapido(true);
        cliente.setCodigo(Integer.parseInt(fCodigo.getText()));
        cliente.setNome(fNome.getText());
        cliente.setCpf(fCpf.getText());
        
        try {
            controller.inserir(cliente);
        } catch (Exception e) {
            showException(e);
        }
        
    }
    
    
    @Override
    protected final void initActions () {
        btSalvar.addActionListener((ActionEvent e) -> {
            cadastrar();
        });
    }
    
    
    
    
